package in.co.OOP;

public class Ractangle {
private double Height = 0.0;
private double width = 0.0;


public double getHeight() {
	return Height;
}
public void setHeight(double height) {
	Height = height;
}

public double getWidth() {
	return width;
}
public void setWidth(double width) {
	this.width = width;
}
public double area() {
	return Height*width;
	
}

}
