package in.co.OOP;

public class AllShape {
	private int Hight;
	private int Width;
	private int base;

	public AllShape(int hight, int width, int base) {
		// super();
		Hight = hight;
		Width = width;
		this.base = base;
	}

	public int getHight() {
		return Hight;
	}

	public int getWidth() {
		return Width;
	}

	public int getBase() {
		return base;
	}

}
